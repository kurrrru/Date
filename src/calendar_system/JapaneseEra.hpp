enum EraCalendar {
    ERA_CALENDAR_JULIAN,
    ERA_CALENDAR_GREGORIAN
};

enum EraAuthority {
    ERA_AUTHORITY_STANDARD,
    ERA_AUTHORITY_SOUTHERN,
    ERA_AUTHORITY_NORTHERN
};

struct EraDate {
    EraCalendar calendar;
    int year;
    int month;
    int day;
};

struct EraMetadata {
    JapaneseEra era;
    EraAuthority authority;
    EraDate start;
    EraDate end;
    bool has_end;
    const char* kanji;
};

std::string era_to_kanji(JapaneseEra e);
const EraMetadata& get_era_metadata(JapaneseEra era);
std::size_t era_count();

